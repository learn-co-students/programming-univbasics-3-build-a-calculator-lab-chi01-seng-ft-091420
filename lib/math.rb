def addition(num1, num2)
sum = num1 + num2
sum
end

def subtraction(num1, num2)
sum = num1 - num2
sum
end

def division(num1, num2)
sum = num1 / num2
sum
end

def multiplication(num1, num2)
sum = num1 * num2
sum
end

def modulo(num1, num2)
rem = num1 % num2
rem
end

def square_root(num)
sq = Math.sqrt(num)
sq
end