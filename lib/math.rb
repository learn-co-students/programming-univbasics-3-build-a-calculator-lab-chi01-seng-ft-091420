def addition(num1, num2)
sum = num1 + num2
return sum 
end

def subtraction(num1, num2)
difference = num1 - num2
return difference
end

def division(num1, num2)
div = num1 / num2
return div 
end

def multiplication(num1, num2)
multiply = num1 * num2
return multiply
end

def modulo(num1, num2)
remainder = num1 % num2
return remainder
end

def square_root(num)
square = Math.sqrt(num)
return square
end